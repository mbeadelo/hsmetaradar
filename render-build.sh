#!/usr/bin/env bash
# Render build script with Playwright dependencies

set -o errexit

# Install system dependencies for Chromium
apt-get update
apt-get install -y \
    libnss3 \
    libnspr4 \
    libatk1.0-0 \
    libatk-bridge2.0-0 \
    libcups2 \
    libdrm2 \
    libdbus-1-3 \
    libxkbcommon0 \
    libxcomposite1 \
    libxdamage1 \
    libxfixes3 \
    libxrandr2 \
    libgbm1 \
    libpango-1.0-0 \
    libcairo2 \
    libasound2 \
    libatspi2.0-0

# Install Node.js dependencies with Yarn
yarn install

# Playwright will install Chromium via postinstall
